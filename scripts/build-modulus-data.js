#!/usr/bin/env node
/*
 * build-modulus-data.js — regenerates src/modulus-data.js from the public
 * VocaLink / Pay.UK modulus-checking tables.
 *
 *   node scripts/build-modulus-data.js
 *
 * Re-run when Pay.UK publishes updated tables (~quarterly). The bundled data
 * keeps PayBatch fully offline. Source files (weights = valacdos, substitutions
 * = scsubtab) are mirrored here; cross-check against vocalink.com if in doubt.
 */
const https = require('https');
const fs = require('fs');
const path = require('path');

// Authentic VocaLink-format tables (space-separated valacdos + scsubtab).
const SOURCES = {
  weights: 'https://raw.githubusercontent.com/alexfilatov/uk_modulus/master/priv/data/valacdos.txt',
  substitutions: 'https://raw.githubusercontent.com/alexfilatov/uk_modulus/master/priv/data/scsubtab.txt'
};

function fetch(url) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      if (res.statusCode !== 200) return reject(new Error(`${url} → HTTP ${res.statusCode}`));
      let body = '';
      res.on('data', (c) => (body += c));
      res.on('end', () => resolve(body));
    }).on('error', reject);
  });
}

function parseWeights(text) {
  return text.split(/\r?\n/).map((l) => l.trim()).filter(Boolean).map((line) => {
    const f = line.split(/[\s,]+/).map((x) => x.trim()).filter(Boolean);
    const [start, end, method] = f;
    const weights = f.slice(3, 17).map(Number);
    const exception = f.length > 17 ? Number(f[17]) : null;
    return { start, end, method, weights, exception };
  });
}

function parseSubstitutions(text) {
  const out = {};
  text.split(/\r?\n/).map((l) => l.trim()).filter(Boolean).forEach((line) => {
    const parts = line.split(/[\s,]+/).filter(Boolean);
    if (parts.length >= 2) out[parts[0]] = parts[1];
  });
  return out;
}

(async () => {
  const [wTxt, sTxt] = await Promise.all([fetch(SOURCES.weights), fetch(SOURCES.substitutions)]);
  const weights = parseWeights(wTxt);
  const substitutions = parseSubstitutions(sTxt);

  const data = { weights, substitutions };
  const out = `/* AUTO-GENERATED by scripts/build-modulus-data.js — do not edit by hand.
 * VocaLink / Pay.UK modulus-checking tables (${weights.length} weight rows,
 * ${Object.keys(substitutions).length} substitutions). Regenerate when Pay.UK updates them. */
const ModulusData = ${JSON.stringify(data)};

if (typeof window !== 'undefined') window.ModulusData = ModulusData;
if (typeof module !== 'undefined' && module.exports) module.exports = ModulusData;
`;
  const dest = path.join(__dirname, '..', 'src', 'modulus-data.js');
  fs.writeFileSync(dest, out, 'utf8');
  console.log(`Wrote ${dest}: ${weights.length} weight rows, ${Object.keys(substitutions).length} substitutions.`);
})().catch((err) => { console.error(err); process.exit(1); });
